package com.guozha.buy.entry.mine.collection;

public class CollectionMenu {
	private int myMenuId;
	private int myDirId;
	private int menuId;
	private String menuName;
	private String menuImg;
	private String cookieWay;
	private int cookieTime;
	private int calories;
	public int getMyMenuId() {
		return myMenuId;
	}
	public void setMyMenuId(int myMenuId) {
		this.myMenuId = myMenuId;
	}
	public int getMyDirId() {
		return myDirId;
	}
	public void setMyDirId(int myDirId) {
		this.myDirId = myDirId;
	}
	public int getMenuId() {
		return menuId;
	}
	public void setMenuId(int menuId) {
		this.menuId = menuId;
	}
	public String getMenuName() {
		return menuName;
	}
	public void setMenuName(String menuName) {
		this.menuName = menuName;
	}
	public String getMenuImg() {
		return menuImg;
	}
	public void setMenuImg(String menuImg) {
		this.menuImg = menuImg;
	}
	public String getCookieWay() {
		return cookieWay;
	}
	public void setCookieWay(String cookieWay) {
		this.cookieWay = cookieWay;
	}
	public int getCookieTime() {
		return cookieTime;
	}
	public void setCookieTime(int cookieTime) {
		this.cookieTime = cookieTime;
	}
	public int getCalories() {
		return calories;
	}
	public void setCalories(int calories) {
		this.calories = calories;
	}
}
